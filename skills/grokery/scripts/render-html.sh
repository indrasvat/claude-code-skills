#!/usr/bin/env bash
#
# Rendering a synthesized markdown brief into a self-contained, dark-mode,
# offline (no-JS, inline-CSS, system-font) HTML file — the grokery equivalent of
# last30days' --emit=html / --synthesis-file. Deterministic fallback; the agent
# may instead hand-craft richer HTML from references/brief-template.html.
#
# Usage: render-html.sh <synthesis.md> [--title "..."] [--out <path>]
set -euo pipefail

usage() { echo "usage: render-html.sh <synthesis.md> [--title STR] [--out PATH]" >&2; exit 2; }

src=""; title=""; out=""
while [ $# -gt 0 ]; do
  case "$1" in
    --title) title="${2:-}"; shift 2 ;;
    --out)   out="${2:-}"; shift 2 ;;
    -h|--help) usage ;;
    --*)     echo "render-html: unknown flag: $1" >&2; usage ;;
    *)       if [ -z "$src" ]; then src="$1"; shift; else echo "render-html: unexpected arg: $1" >&2; usage; fi ;;
  esac
done
[ -n "$src" ] && [ -f "$src" ] || { echo "render-html: markdown file required" >&2; usage; }
[ -n "$title" ] || title="grokery brief"
[ -n "$out" ] || out="${src%.md}.html"

GROKERY_TITLE="$title" python3 - "$src" "$out" <<'PY'
import html, os, re, sys

src, out = sys.argv[1], sys.argv[2]
title = os.environ.get("GROKERY_TITLE", "grokery brief")
with open(src, encoding="utf-8") as fh:
    lines = fh.read().splitlines()

def inline(s: str) -> str:
    s = html.escape(s)
    s = re.sub(r'`([^`]+)`', r'<code>\1</code>', s)
    s = re.sub(r'\*\*([^*]+)\*\*', r'<strong>\1</strong>', s)
    s = re.sub(r'\*([^*]+)\*', r'<em>\1</em>', s)
    s = re.sub(r'\[([^\]]+)\]\((https?://(?:[^()\s]|\([^()]*\))+)\)', r'<a href="\2">\1</a>', s)
    s = re.sub(r'&lt;(https?://[^&]+)&gt;', r'<a href="\1">\1</a>', s)
    return s

body, in_list, in_code = [], False, False
def close_list():
    global in_list
    if in_list:
        body.append("</ul>"); in_list = False

for ln in lines:
    raw = ln.rstrip()
    if raw.lstrip().startswith("```"):
        close_list()
        if in_code:
            body.append("</code></pre>"); in_code = False
        else:
            body.append("<pre><code>"); in_code = True
        continue
    if in_code:
        body.append(html.escape(raw)); continue
    if not raw.strip():
        close_list(); continue
    if raw.startswith("### "):
        close_list(); body.append(f"<h3>{inline(raw[4:])}</h3>")
    elif raw.startswith("## "):
        close_list(); body.append(f"<h2>{inline(raw[3:])}</h2>")
    elif raw.startswith("# "):
        close_list(); body.append(f"<h1>{inline(raw[2:])}</h1>")
    elif raw.strip() in ("---", "***"):
        close_list(); body.append("<hr>")
    elif re.match(r'^\s*[-*]\s+', raw):
        if not in_list:
            body.append("<ul>"); in_list = True
        body.append(f"<li>{inline(re.sub(r'^\s*[-*]\s+', '', raw))}</li>")
    else:
        close_list(); body.append(f"<p>{inline(raw)}</p>")
close_list()
if in_code:
    body.append("</code></pre>")

doc = f"""<!DOCTYPE html>
<html lang="en"><head><meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>{html.escape(title)}</title>
<style>
:root{{color-scheme:dark}}
*{{box-sizing:border-box}}
body{{margin:0;background:#000;color:#e7e9ea;
 font:16px/1.6 -apple-system,BlinkMacSystemFont,"Segoe UI",Inter,Roboto,Helvetica,Arial,sans-serif;
 letter-spacing:-.003em}}
.wrap{{max-width:760px;margin:0 auto;padding:32px 20px 80px}}
.badge{{display:inline-block;font-size:12px;font-weight:800;letter-spacing:.08em;text-transform:uppercase;
 color:#fff;background:linear-gradient(135deg,#1d9bf0,#7856ff);padding:5px 12px;border-radius:999px}}
h1{{font-size:30px;font-weight:850;letter-spacing:-.02em;margin:18px 0 4px}}
h2{{font-size:21px;font-weight:800;margin:34px 0 6px;padding-bottom:6px;border-bottom:1px solid #2f3336}}
h3{{font-size:16px;font-weight:750;color:#7cc6f7;margin:22px 0 4px}}
p{{color:#c8cdd2;margin:8px 0}}
ul{{margin:8px 0;padding-left:20px}}
li{{margin:5px 0;color:#c8cdd2}}
a{{color:#1d9bf0;text-decoration:none}}a:hover{{text-decoration:underline}}
code{{font-family:ui-monospace,"JetBrains Mono",Menlo,Consolas,monospace;font-size:.86em;
 background:#16181c;border:1px solid #2f3336;border-radius:5px;padding:.05em .4em;color:#cfd9e0}}
strong{{color:#f3f5f6}}
hr{{border:none;border-top:1px solid #2f3336;margin:22px 0}}
.foot{{margin-top:40px;padding-top:16px;border-top:1px solid #2f3336;color:#71767b;font-size:13px}}
@media print{{body{{background:#fff;color:#000}}a{{color:#0a5}}}}
</style></head>
<body><div class="wrap">
<span class="badge">grokery · signal brief</span>
{os.linesep.join(body)}
<div class="foot">Generated by <code>grokery</code> — grok-powered cross-platform signal scan. · Ranked by engagement, not SEO.</div>
</div></body></html>
"""
with open(out, "w", encoding="utf-8") as fh:
    fh.write(doc)
print(out)
PY
